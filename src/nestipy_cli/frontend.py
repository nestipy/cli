# The Inertia root template lives in the BACKEND (views/index.html). React needs the
# Vite React-refresh preamble (viteReactRefresh) in dev or @vitejs/plugin-react throws;
# Vue/Svelte must NOT include it. So it is written per chosen framework.
def _views_index(framework: str) -> str:
    refresh = "{{ viteReactRefresh() }}\n" if framework == "react" else ""
    return (
        "<!doctype html>\n"
        '<html lang="en">\n'
        "<head>\n"
        '    <meta charset="UTF-8" />\n'
        '    <link rel="icon" type="image/svg+xml" href="/vite.svg" />\n'
        '    <meta name="viewport" content="width=device-width, initial-scale=1.0" />\n'
        "    <title>Nestipy + Inertia.js</title>\n"
        "    {{ inertiaHead() }}\n"
        "</head>\n"
        "<body>\n"
        f"{refresh}"
        "{{ inertiaBody() }}\n"
        "</body>\n"
        "</html>\n"
    )


