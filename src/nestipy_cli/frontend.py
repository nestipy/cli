def npm_available() -> bool:
    return shutil.which("npm") is not None


def _run(cmd: list[str], cwd: str) -> None:
    subprocess.run(cmd, cwd=cwd, check=True)


def _write(path: str, content: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as handle:
        handle.write(content)


def _patch_backend_entrypoint(destination: str, entry_file: str) -> None:
    """Set `entrypoint_filename` on the backend's InertiaConfig to match the entry."""
    app_module = os.path.join(destination, "src", "app_module.py")
    if not os.path.exists(app_module):
        return
    with open(app_module, encoding="utf-8") as handle:
        text = handle.read()
    if "entrypoint_filename" in text or "InertiaConfig(" not in text:
        return
    text = text.replace(
        "InertiaConfig(",
        f'InertiaConfig(entrypoint_filename="{entry_file}", ',
        1,
    )
    with open(app_module, "w", encoding="utf-8") as handle:
        handle.write(text)


def scaffold_inertia_frontend(
    destination: str,
    framework: str,
    dir_name: str = "inertia",
    install: bool = True,
) -> None:
    """Create a Vite app for `framework` inside `<destination>/<dir_name>` and wire Inertia.

    Raises CalledProcessError / FileNotFoundError on tooling failure so the caller can
    report manual fallback steps.
    """
    fw = framework.lower()
    if fw not in FRAMEWORKS:
        raise ValueError(f"Unsupported framework: {framework}")

    template = _VITE_TEMPLATE[fw]
    frontend_path = os.path.join(destination, dir_name)
    entry = _ENTRY[fw]
    ext = _PAGE_EXT[fw]

    # 1. scaffold the base Vite app (non-interactive)
    _run(
        ["npm", "create", "vite@latest", dir_name, "--", "--template", template],
        cwd=destination,
    )

    # 2. drop the default boilerplate entry/app so Inertia owns the mount
    for stale in ("src/App.tsx", "src/App.vue", "src/App.svelte", "src/app.css"):
        stale_path = os.path.join(frontend_path, stale)
        if os.path.exists(stale_path):
            os.remove(stale_path)

    # 3. write the Nestipy Vite plugin, Inertia entry, vite config and pages
    _write(os.path.join(frontend_path, "nestipy.inertia.ts"), _NESTIPY_PLUGIN)
    _write(os.path.join(frontend_path, entry), _ENTRY_CONTENT[fw])
    _write(
        os.path.join(frontend_path, "vite.config.ts"),
        _VITE_CONFIG[fw].format(entry=f"./{entry}"),
    )
    _write(os.path.join(frontend_path, "src", "Pages", f"Index.{ext}"), _INDEX_PAGE[fw])
    _write(os.path.join(frontend_path, "src", "Pages", f"About.{ext}"), _ABOUT_PAGE[fw])

    # 4. write the backend Inertia root template matching the chosen framework
    _write(os.path.join(destination, "views", "index.html"), _views_index(fw))

    # 5. point the backend InertiaConfig at the right client entry. The adapter loads
    #    `{dev_url}/src/{entrypoint_filename}` in dev and the matching manifest key in
    #    prod; the default is main.tsx, so Vue/Svelte (main.ts) must override it.
    _patch_backend_entrypoint(destination, os.path.basename(entry))

    # 6. install deps + the Inertia adapter for this framework
    if install:
        _run(["npm", "install"], cwd=frontend_path)
        _run(["npm", "install", _INERTIA_PKG[fw]], cwd=frontend_path)
