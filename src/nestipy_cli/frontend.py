"""Generate an Inertia.js frontend (React / Vue / Svelte) at scaffold time.

Unlike the backend (copied from a template), the frontend is generated by running
the real framework tooling (`npm create vite`) and then wiring Inertia on top, so
the generated app always tracks the latest framework + Inertia versions.
"""

import os
import shutil
import subprocess

FRAMEWORKS = ("react", "vue", "svelte")

# create-vite template + matching Inertia adapter + entry filename per framework
_VITE_TEMPLATE = {"react": "react-ts", "vue": "vue-ts", "svelte": "svelte-ts"}
_INERTIA_PKG = {
    "react": "@inertiajs/react",
    "vue": "@inertiajs/vue3",
    "svelte": "@inertiajs/svelte",
}
_ENTRY = {"react": "src/main.tsx", "vue": "src/main.ts", "svelte": "src/main.ts"}
_PAGE_EXT = {"react": "tsx", "vue": "vue", "svelte": "svelte"}

_NESTIPY_PLUGIN = """import type { PluginOption } from "vite";

type Input = {
    entry: string;
    ssr?: string;
    manifest?: string;
};
type NestipyPlugin = (options: Input) => PluginOption;

const nestipyVite: NestipyPlugin = ({ entry, ssr, manifest = "manifest.json" }) => {
    return {
        name: "nestipy-vite-plugin",
        config: (config, env) => {
            return {
                ...config,
                build: {
                    manifest: env.isSsrBuild ? false : manifest,
                    outDir: env.isSsrBuild ? "dist/ssr" : "dist",
                    rollupOptions: {
                        input: env.isSsrBuild && ssr ? ssr : entry,
                    },
                },
            };
        },
    };
};

export default nestipyVite;
"""

